#include "libft.h"

char	*ft_strdup(const char *src)
{
	char	*dest;
	int		count;

	count = 0;
	dest = (char *)ft_memalloc((ft_strlen((char *)src) + 1) * sizeof(char));
	while (src[count] != '\0')
	{
		dest[count] = src[count];
		count++;
	}
	dest[count] = '\0';
	return (dest);
}
